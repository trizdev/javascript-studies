//While Loops

let num =0;

while (num < 100) { //while this is true
  num += 1; //run this part of the code
  console.log(num); //appear on the console
}

// For loop

for (let num = 0; num < 100; num++){
  console.log(num);
}

//they both are the same thing but the second is easier to write and read.